import UIKit

//Basica estensione dimostrativa

extension UILabel {
    
    ///modifica testo e backgroundColor
    func cambia(testo: String, eBackground: UIColor){
        
        self.text = testo
    
        self.backgroundColor = eBackground
        
    }
    
    
}
